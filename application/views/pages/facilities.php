          <h2>Facilities</h2>
		  
		  <div style="font-size:16px; text-align:justify;">
		  <h3>Sports and Games :</h3>
There are arrangements for indoor and outdoor sports and games - under the guidance of a Teacher-in-charge of games and sports and an experienced Coach. The College has a playground of its own in the Kolkata Maidan and a beautiful pavillion - the first pavilllion in the Maidan for any college of Kolkata.<br /><br />


<h3>Library :</h3>
The College has a Library with a large number of rare books. Facilities of UGC and Text Book Libraries are also available. The present Reading Room has the prospect of expansion for larger accommodations. There is a spacious Reading Room in the Library for the use of students to enable them in their reference work. Computerization is being undertaken. The classification and cataloguing of books are done according to D D C (Dewy Decimal Classification) and AACR2 (Anglo-American Catalogueing Rules). The College Library consists of a rich collection of rare books, journals, and periodicals which include the latest publications on various subjects. Internet facility is available for the students and teachers<br /><br />

<h3>Common Room :</h3>
There are two Common Rooms - one for the boys and the other exclusively for the girls. There are arrangements for various indoor games in the common Rooms - table tennis, carrom and chess. This facilitates recreational activities.<br /><br />

<h3>Extra-Curricular Activities :</h3>
The College encourages the following types of extra-curricular activities of students as a part of college education : Formation of habits in civic responsibility, the broadening of outlook and sympathies through varied associations and cultural development through participation in the intellectual and aesthetic life of the College and society at large.<br /><br />

<h3>Students' Union :</h3>
It is composed of student-representatives from all classes - one member from the teaching faculty as President, teachers as sectional presidents. The College authority encourages the development of a healthy corporate life in the college. Apart from organizing various socio-cultutal activities, the union plays a positive role in furthering the development programmes of the institution.<br /><br />

All students of the College are members of the Students' Union which functions through a Representative Committee elected by the students of different classes. The aim of which is that different communities may mix freely and work in close co-operation with one another in an atmosphere of good will and harmony. Blood Donation Camps are organized annually by the Union. The Union fosters the growth of a healthy communication among students through various cultural programmes, sports and seminars, raises funds for social service and relief work, as well as rendering financial help to the poor and deserving students of the College. It is managed by the students under the direction of the teacher who is the President of the College students' Union. The Students' Union is a vibrant entity in the college.<br /><br />

<h3>College Canteen :</h3>
The canteen caters various types of fast food and light refreshments and snacks at an affordable price.<br /><br />

<h3>Nature Club :</h3>
It provides the scope for establishing harmony between man and nature, which is strengthened by an understanding and comonness of purpose among students, teachers, and non-teaching staff as well as well-wishers of the neighbourhood through annually organized tree-plantation.<br /><br />

<h3>National Service Scheme :</h3>
NSS units of the College organize various types of social work : Plantation of Trees, Biological and Pathological Testing, Seminar on AIDS, Hygiene and Public Health Programme, Environmental Consciousness and Scientific Awareness Programmes, Employment Avenues, Literacy Programme, Health Check-up Programmes, Market Surveys, Educational Tours, Socio-Economic Surveys of slum areas and various other special campaigns. The two units of NSS have been working for encouraging the tradition of social service in the hearts of the students by working with the local communities on certain specific concrete programmes.<br /><br />

<h3>College Societies :</h3>
The Objective of these Societies is to develop power of observation, elocution, debating and writing on educational, social and other topics of general interest.<br /><br />

<h3>College Magazine :</h3>
The College publishes an Annual Magazine enriched with contribution from members of the staff and the students on various subjects. It encourages the students to contribute their own articles for publication. The magazine helps to kindle the creative talents of the students.<br /><br />

College Annual Social Function :
The college Annual Social Function is organized by the Students' Union under the guidance of a teacher.<br /><br />

<h3>Cultural Programmes :</h3>
Rabindra Jayanti, Matri Bhasha Divas, Netaji's Birthday, Republic Day, Independence Day, Founder's Day and Seminars on various aspects are organized by the college and Students' Union. Learning becomes fruitful and joyful by these means.<br /><br />

<h3>Alumni Association :</h3>
The newly incepted association helps to bridge the gap between the glorious past and the present. It facilitates to strengthen the bonds of fellow-feeling with the students of yester- years.<br /><br />

<h3>College NCC :</h3>
NCC is compulsory for all the students of this college. The students are required to get themselves familiar with NCC rules from NCC Officers. Facilities are given to new entrants. Various facilities like Republic Day Camp (RDC), National Integrated Camp (NIC), Trekking and Mountaineering and Army Attachment Camp inculcate among the young students a sense of harmonious living and rouse among them a patriotic feeling and a sense of discipline.<br /><br />

<h3>College Auditorium :</h3>
The College has a spacious auditorium where various functions organized by the students and the staff are held. The auditorium was named Prasanta Kumar Bose Memorial Hall on 22, December 1995. Seminars, Workshops, Cultural Programmes, Educational Symposia, Inter College Competitions, Social, Debates are arranged here for the enlightenment not only of the students but also the general public.<br /><br />
		  
