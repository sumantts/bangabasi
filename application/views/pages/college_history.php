          <h3>Brief History of the College</h3>
		  
		  <p style="font-size:16px; text-align:justify;">
		  Principal Prasanta Kumar Bose, the illustrious and worthy son of Acharya Girish Chandra Bose, founder of Bangabasi College, an eminent educationist of Bengal, founded the evening section of the Bangabasi College for the needy employed students in 1940 to teach Commerce.<br /><br />


In 1944 Principal Bose opened up the Arts and Science faculties in the evening time. Finally employed women were also enrolled as students by Principal Bose. Through the introduction of the Phase Reduction Scheme of the University Grants Commission, Bangabasi Evening College came into a separate existence on 11 April 1965, under the guidance and direction of Principal Prasanta Kumar Bose. At that time there were ten thousand students in Bangabasi College, but the number of students in the colleges of West Bengal had to be reduced as per the University Grant Commission scheme. In accordance with the Scheme, Bangabasi College was split up into three seperate Colleges. The evening shift was named Bangabasi  Evening College under a separate Governing Body. During the Second World War a branch of the Bangabasi College was established in Kushtia (now in Bangladesh) in 1942, as the people of Kolkata were troubled by the bombing of Japanese troops. After the Second World War it was closed.<br /><br />


Bangabasi Evening College is located in the vicinity of Sealdah Station in Kolkata. Bangabasi College is an educational institution with a rich cultural tradition in its background. It had its genesis in the prophetic vision and spirit of sacrificial dedication of Acharya Girish Chandra Bose. At the outset, Bangabasi Evening College was founded under private management in 1887 and had as its guiding inspiration the genius of Pandit Iswar Chandra Vidyasagar. The aim of the college was basically two-fold - to cater to the growing demand for higher education and to nurture the spirit of nationalism in the minds of young learners under colonical yoke. It marked an indirect protest - challenging the narrow educational policy of the Hunter Commission.<br /><br />


This institution seminally emerged from Bangabasi School, founded by Acharya Girish Chandra Bose in 1855 in Bowbazar Street. The college moved into   its present premises at 19, Scott Lane (now Raj Kumar Chakraborty Sarani) in 1903.<br /><br />


The College derived its name from the then patriotic newspaper Bangabasi. Acharya Girish Chandra Bose, a man of astounding personality and farsight, filled with a missionary zeal of patriotic reverberation, planted this sapling at 16, Bowbazar Street on 1 June 1887, with only five teachers and twelve students. The sapling withstood the storms and stresses of the decades and now like a very big banyan tree gives shade and extends shelter to several thousand students of merit and ability, hundreds of teachers and with real integrity and acquisition and many non-teachers of admirable efficiency. The college has a composite amalgum of academics and student community.<br /><br />


During the days of alien rule the institution served as a refuge for political sufferers - students as well as teachers. The nationalist spirit nurtured by the college was amply evident in the active participation of students and teachers in the agitation against the partition of Bengal in 1905 and also in the Civil Disobedience Movement culminating in the supreme sacrifice of martyr Jatindra Nath Das, an undergraduate student of the college, in his epic fast for unto death in Lahore Jail on 13 September 1929.<br /><br />


Some of the illustrious teachers of this College such as Ladly Mohan Mitra and Satyananda Roy, were the pioneers to extend the facilities of higher education to the toiling mass of the working students who were compelled to engage themselves in different jobs in the day-time and were deprived of College education, by opening the evening classes under the banner of Bangabasi Evening College.
		  
