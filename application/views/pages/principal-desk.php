              <p  style="text-align:justify;">Welcome to our glorious institution Bangabasi Evening College, 19, Rajkumar ChakrabortySarani, Kolkata - 700009, accredited by National Assessment and Accreditation Council (NAAC) in 2008. We have applied for
reaccreditation. This college was established in 1965 under the purview of affiliation to the University of Calcutta as a twig of historic Bangabasi College. Late Acharya Girish Chandra Bose, the eminent educationist and great Social Reformer, was the founder of Bangabasi College. He established the institutions - 'Bangabasi School' (1885 AD.) and 'Bangabasi College' (1887 AD.), with the sole objective of promoting the cause of education in Bengal during the nineteenth century renaissance in India. After the independence of India in 1947 A.D. there was an influx of young talents from the then East Bengal (now Bangladesh) and elsewhere. In order to accommodate them, as well as to ease the tremendous pressure of admission to the College, three new shifts like Morning, Evening and Commerce had to be opened. Prof. Prasanta Kumar Bose, son of Acharya Girish Chandra Bose was the founder Principal of Bangabasi Evening College. Now it offers Honours degree in Bengali, English, History, Political Science, Sanskrit, Philosophy, Chemistry, Mathematics, Anthropology, Economics, Physiology and Accountancy. Other subjects include Hindi, Physics, Geography Botany and Zoology. It is a great pleasure to announce that Bangabasi Evening College has recently introduced Post Graduate Department of Mathematics from 2015-2016 Session, as a regular course in M.Sc under the University of Calcutta. Moreover, in the year 2015 we have applied for Botany Hons Course, when we get permission from Calcutta University and Higher Education Council, we will announce soon through webpage of the college.</p>

